public class OperadoresIncrementables {
    public static void main(String[] args) {
        //Preincremento
        int i = 1;
        int j = ++i;
        System.out.println(j);

        //postincremento
        i = 2;
        j = i++;
        System.out.println(j);

        //predecremento
        i =3;
        j = --i;
        System.out.println(j);

        //postdecremento
        i=4;
        j=i--;
        System.out.println(j);
    }
}
