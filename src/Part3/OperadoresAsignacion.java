package Part3;

public class OperadoresAsignacion {

    public static void main(String[] args) {
        int i = 5;

        i += 2;
        System.out.println(i);

        i -= 4;
        System.out.println(i);

        i *=2;
        System.out.println(i);

        i /= 5;
        System.out.println(i);
    }
}
